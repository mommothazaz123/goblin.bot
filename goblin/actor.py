class Actor:
    def __init__(self, **data):
        self.name: str = data['Name']
        self.cr: int = data['CR']
        self.max_hp: int = data['MaxHP']
        self.ac: int = data['AC']
        self.stats = data['Stats']
        self.tags = data['Tags']
        self.size: int = data['Size']
        self.movement = data['Movement']
        self.skills = data['Skills']
        self.saves = data['Saves']
        self.senses = data['Senses']
        self.languages = data['Languages']
        self.actions = data['Actions']
        self.immune = data['Immune']
        self.resist = data['Resist']
        self.vulnerable = data['Vulnerable']
        self.conditions = data['Conditions']
        self.c_info = data['CInfo']
        self.vis = data['Vis']
        self.events = data['Events']
        self.e_info = data['EInfo']
        self.concentration: str = data['Concentration']
        self.act_econ = data['ActEcon']
        self.fact = data['Face']
        self.t_stats: list = data.get('TStats', [])
        self.target: str = data['Target']
        self.id: str = data['ID']
        self.init = data.get('Init')
